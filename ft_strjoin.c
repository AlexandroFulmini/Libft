#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*res;

	if (!s1 || !s2)
		return (NULL);
	if (!(res = malloc(sizeof(char) *
		(ft_strlen((char*)s1) + ft_strlen((char*)s2) + 1))))
		return (NULL);
	ft_strcpy(res, (char*)s1);
	ft_strcat(res, (char*)s2);
	return (res);
}
